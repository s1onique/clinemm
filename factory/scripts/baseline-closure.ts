#!/usr/bin/env bun
/**
 * ACT-CLINEMM-FORK-BASELINE01-CORRECTION11 — Closure logic (pure).
 *
 * The renderer (`render-baseline-report.ts`) imports the helpers from this
 * module so the verdict logic is independently testable. Importing this
 * module has no side effects — it does not read the working tree, write any
 * file, or spawn git. The renderer's main entry performs I/O and calls into
 * `computeClosure` / `checkEvidence`.
 *
 * Policy (CORRECTION11, fail-closed + non-self-referential subject-tree +
 * separated execution identity + per-command drift attestation):
 *
 *   FAIL      evidence is missing, malformed, stale-bound, hash-invalid,
 *             multi-tree, command-set-mismatched, symlinked,
 *             outside-evidence-dir, self-referential, on-a-dirty-worktree,
 *             split between subject and execution identity, captured
 *             with drift between/within commands, or recorded with
 *             malformed paths; OR there are UNKNOWN-classified failures
 *             with no investigation note.
 *   PARTIAL   evidence is internally valid and command-set-exact (incl. per-
 *             record equality), the UNKNOWN policy is satisfied, but at
 *             least one declared baseline requirement (R4/R5/R6/R7/R16)
 *             remains open.
 *   PASS      every requirement is satisfied and all mandatory commands
 *             pass on the binding host.
 *
 * CORRECTION11 binding model — runner drift attestation:
 *
 *   Tracked files generated by the verification workflow are still
 *   structurally excluded from the subject tree (CORRECTION10). The
 *   runner now records four additional attestations:
 *
 *     subject_tree_oid_before / subject_tree_oid_after
 *                          — filtered OID captured before and after
 *                            the matrix. Required to be equal.
 *     executionIdentityValid
 *                          — `git cat-file -e <head>^{commit}` and
 *                            `git cat-file -e <tree>^{tree}` both
 *                            succeed, AND
 *                            `git rev-parse <head>^{tree}` equals
 *                            `execution_tree_oid`.
 *     worktreeInputsCleanBefore / worktreeInputsCleanAfter
 *                          — `boolean | null` tri-state; the runner
 *                            captures path-aware cleanliness (every
 *                            path except declared expected outputs)
 *                            before and after the matrix. Required to
 *                            be `true` for verdict to leave FAIL on
 *                            these grounds. `null` means "not recorded".
 *     perCommandDriftChecked
 *                          — every command row has equal before/after
 *                            HEAD/tree/subject fields, captured by the
 *                            runner immediately around the command.
 *
 *   These fields were added because the CORRECTION10 reviewer found
 *   that the runner:
 *
 *     1. Sampled cleanliness only after the matrix (R1).
 *     2. Could not satisfy post-run cleanliness with tracked outputs
 *        on the same worktree (R2).
 *     3. Only syntax-checked the execution OIDs, not that they form a
 *        valid Git object pair (R3).
 *     4. Stamped every command row with the same `execTree` captured
 *        before the matrix, so HEAD/tree drift mid-run was silent (R4).
 *     5. Captured the subject only after execution, not before (R5).
 *     6. Recorded stdout/stderr paths as repo-relative rather than
 *        evidence-dir-relative, leaving the manifest path model
 *        inconsistent (R6).
 *
 *   CORRECTION11 closes each of these by failing closed on the new
 *   reason codes:
 *     EXECUTION_IDENTITY_INVALID
 *     WORKTREE_INPUTS_DIRTY_BEFORE
 *     WORKTREE_INPUTS_DIRTY_AFTER
 *     SUBJECT_DRIFT
 *     REPOSITORY_DRIFT
 *     MANIFEST_PATH_OUTSIDE_EVIDENCE
 *
 * Every evidence dimension must hold simultaneously for the verdict to
 * not be FAIL on evidence grounds:
 *
 *   - evidence.json exists and was JSON-decodable
 *   - subject binding matches (subject_tree_oid comparison)
 *   - execution identity is well-formed and its head/tree are related
 *     (`head^{tree} == tree`); the bundle's execution_tree_oid equals
 *     every command's tree_oid, and there is exactly one tree
 *   - subject and execution identity remained stable across the matrix
 *     (no per-command drift; before/after captured and equal)
 *   - subject inputs were clean before and after the matrix
 *   - hash manifest is well-formed, contained under the evidence
 *     directory, duplicate-free, contains no absolute/traversal paths,
 *     and every declared payload path exists, matches its declared
 *     SHA-256, and is not a symlink
 *   - on-disk non-control files in the evidence directory are all
 *     declared in the manifest (hashes.sha256 itself is a control file
 *     and exempt)
 *   - every executed command has exactly one matching evidence row, no
 *     duplicates in either set, no malformed rows, and per-ID field
 *     equality (status, head/tree, exit_code, timeout, hashes,
 *     classification)
 *
 * Note: `evidence.head_oid` is *informational*. Its well-formedness is
 * documented for traceability but does NOT drive closure — only the
 * subject tree and execution identity do.
 */

