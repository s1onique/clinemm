/**
 * The complete integrity picture for the detached evidence bundle. Every
 * dimension must be satisfied for the verdict to not be FAIL on evidence
 * grounds; see `isEvidenceOk` and `computeClosure`.
 */
export interface EvidenceView {
	/** The evidence.json file existed and was JSON-decodable. */
	exists: boolean;
	/** Information-only: evidence.head_oid is a syntactically valid 40-char lowercase hex OID. Not in the closure conjunction. */
	headOidWellformed: boolean;
	/** Subject binding holds: `subject_tree_oid` matches the renderer's freshly-computed filtered tree. */
	treeMatches: boolean;
	/** Whether the bundle used the new subject-tree contract (true) or fell back to legacy literal `tree_oid`/`HEAD^{tree}` (false). */
	subjectTreeContract: boolean;
	/** Whether the renderer could compute the current filtered subject-tree OID. False ⇒ closure must fail (SUBJECT_TREE_COMPUTATION_FAILED). */
	subjectTreeComputationOk: boolean;
	/** Whether the bundle carries the new execution-identity fields (CORRECTION10/11). False ⇒ closure must fail (EXECUTION_IDENTITY_MISSING). */
	executionIdentityRecorded: boolean;
	/** Whether `evidence.execution_head_oid` is a syntactically valid OID. */
	executionHeadOidWellformed: boolean;
	/** Whether `evidence.execution_tree_oid` is a syntactically valid OID. */
	executionTreeOidWellformed: boolean;
	/**
	 * CORRECTION11: `git cat-file -e <head>^{commit}` and
	 * `git cat-file -e <tree>^{tree}` succeed AND
	 * `git rev-parse <head>^{tree}` equals `execution_tree_oid`.
	 */
	executionIdentityValid: boolean;
	/** All command rows have a single execution-tree value, matching `evidence.execution_tree_oid`. */
	executionTreeBound: boolean;
	/** The set of distinct execution-tree values across command rows. Length must be exactly 1. */
	executionTrees: string[];
	/**
	 * CORRECTION11 tri-state cleanliness: `true` if recorded-and-clean,
	 * `false` if recorded-and-dirty, `null` if the field is absent.
	 */
	worktreeInputsCleanBefore: boolean | null;
	worktreeInputsCleanAfter: boolean | null;
	/**
	 * CORRECTION11: every command row records equal
	 * `head_oid_before`/`head_oid_after`, `tree_oid_before`/`tree_oid_after`,
	 * and `subject_tree_oid_before`/`subject_tree_oid_after`, each
	 * pinning the bundle's execution identity. This field collapses the
	 * per-command check into a single boolean on the bundle.
	 */
	perCommandDriftChecked: boolean;
	/**
	 * CORRECTION11: the bundle records both `subject_tree_oid_before`
	 * and `subject_tree_oid_after`, both equal to the recorded
	 * `subject_tree_oid`. Indicates the subject identity was stable
	 * across the matrix.
	 */
	subjectStableAcrossMatrix: boolean;
	/**
	 * CORRECTION11: the bundle records `expected_output_paths` and the
	 * manifest declares every declared path. Indicates the runner's
	 * evidence-directory-relative path contract is honored.
	 */
	manifestContractHonored: boolean;
	/** Manifest is well-formed and every declared path matches its declared hash. */
	hashManifestValid: boolean;
	missingFiles: PathDiagnostic[];
	unexpectedFiles: PathDiagnostic[];
	hashMismatches: HashMismatch[];
	malformedLines: MalformedLine[];
	duplicatePaths: DuplicatePath[];
	commandSetExact: boolean;
	duplicateEvidenceCommandIds: DuplicatePath[];
	duplicateExecutedCommandIds: DuplicatePath[];
	commandRecordMismatches: CommandRecordMismatch[];
	rejectedManifestPaths: PathDiagnostic[];
	outOfEvidenceDirPaths: PathDiagnostic[];
	malformedEvidenceCommandRows: number;
	malformedExecutedCommandRows: number;
	decodeError: string | null;
}

export interface ClosureInput {
	evidence: EvidenceView;
	unknownFailures: string[];
	unknownFailureCount: number;
	mandatoryPass: number;
	mandatoryFail: number;
	mandatoryApplicable: number;
	affectedScopePass: number;
	affectedScopeFail: number;
	affectedScopeApplicable: number;
	r4Satisfied: boolean;
	r5Satisfied: boolean;
	r6Satisfied: boolean;
	r7Satisfied: boolean;
	r16Satisfied: boolean;
}

export interface ClosureResult {
	verdict: Verdict;
	evidenceOk: boolean;
	reasonCodes: ReasonCode[];
	unknownFailureCount: number;
}

