// ---------- helpers ----------------------------------------------------------

function countBy<T>(items: T[], key: keyof T | ((t: T) => string | null | undefined)): Record<string, number> {
	const out: Record<string, number> = {};
	for (const it of items) {
		if (it == null) continue;
		const v = typeof key === "function" ? (key as any)(it) : (it as any)[key];
		if (v == null) continue;
		const k = String(v);
		out[k] = (out[k] ?? 0) + 1;
	}
	return out;
}

function parseCsvCount(s: string): number {
	let n = 0;
	for (const line of s.split("\n")) if (line.trim().length > 0) n++;
	return n;
}

function listDir(p: string): number {
	try {
		let n = 0;
		for (const f of require("node:fs").readdirSync(p)) if (f) n++;
		return n;
	} catch { return 0; }
}

function renderFailureTable(counts: Record<string, number>): string {
	const entries = Object.entries(counts).sort(([a], [b]) => a.localeCompare(b));
	if (entries.length === 0) return "_No executed commands._";
	return `| Classification | Count |\n| --- | ---: |\n${entries.map(([k, v]) => `| \`${k}\` | ${v} |`).join("\n")}`;
}

function computeClosureStatus(p: { passN: number; failN: number; mandatoryClass: number; unknownFailures: string[]; unknownCount: number; hasR7Evidence: boolean }): "PASS" | "PARTIAL" | "FAIL" {
	if (p.unknownCount > 0 || p.unknownFailures.length > 0) return "FAIL";
	if (!p.hasR7Evidence) return "PARTIAL";
	if (p.failN > 0) return "PARTIAL";
	return "PASS";
}

function closureReason(_closure: string, _p: any): string {
	return "See the \`Disposition\` and \`Closure decision (mechanical)\` tables; arithmetic is auto-derived from \`verification-results.json\`.";
}

function closureDecision(closure: string): string {
	if (closure === "FAIL") {
		return `Closure is **FAIL**. The \`UNKNOWN\` policy in \`factory/scripts/run-verification.ts\` mandates that every failure be classified; UNKNOWN-without-investigation blocks closure. Reproduce the UNKNOWN against the canonical clean install (Bun 1.3.13, Node 22, frozen lockfile) and assign one of \`TOOLCHAIN-DRIFT\`, \`NETWORK-DEPENDENT\`, \`INSTALL-INCOMPLETE\`, or \`UPSTREAM-REPRODUCIBLE\`.`;
	}
	if (closure === "PARTIAL") {
		return `Closure is **PARTIAL**. The mandatory/affected-scope matrix produced failing results on the primary host; cross-platform CI evidence is absent. Until the Linux \`ext-vscode-test.yml\` matrix is dispatched and passes, \`R7\` is not satisfied and \`PASS\` is not possible.`;
	}
	return `Closure is **PASS**. Every mandatory command passed on the applicable authoritative host(s), no \`UNKNOWN\` failure exists, and the production tree is byte-identical to upstream.`;
}

function successorBlock(p: { passN: number; failN: number; mandatoryClass: number; cmtSummary: string[]; unknownCount: number }): string {
	const lines: string[] = [];
	lines.push("`ACT-CLINEMM-EXECUTABLE-CONTRACT-FIRST01` may start only when:");
	lines.push("");
	lines.push("1. The Linux matrix is dispatched and PASS-clean (R7).");
	lines.push("2. Every UNKNOWN is reproduced and classified (TOOLCHAIN-DRIFT / NETWORK-DEPENDENT / INSTALL-INCOMPLETE / UPSTREAM-REPRODUCIBLE).");
	lines.push("3. The structural baseline is regenerated from the upstream tree to eliminate self-contamination.");
	lines.push("4. The verifier is rewritten to detect additions/deletions/modes against the selected OID (R4).");
	lines.push("5. Real JSON Schema validation is implemented in `verify-baseline.ts` (R5).");
	lines.push("6. Verification discovery is replaced with a real source-derived scan (R16).");
	lines.push("");
	lines.push(`Current blockers: pass=${p.passN}/${p.mandatoryClass} mandatory, fail=${p.failN}, unknown=${p.unknownCount}, cmt unknown=${JSON.stringify(p.cmtSummary)}.`);
	return lines.join("\n");
}
